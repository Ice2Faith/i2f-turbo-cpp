typedef unsigned int * GPPOINTER;

#define PERIPH_BASE 		((unsigned int)0x40000000) 		//0x4000 0000 external divice base address
#define APB2PERIPH_BASE 	(PERIPH_BASE+0x00010000) 		//0x4001 0000 apb2 bus base address
#define GPIOC_BASE 			(APB2PERIPH_BASE+0x1000) 		//0x4001 1000 gpioc base address
#define GPIOC_CRL			(GPPOINTER)(GPIOC_BASE+0x00)		//32bit=4byte
#define GPIOC_CRH			(GPPOINTER)(GPIOC_BASE+0x04)
