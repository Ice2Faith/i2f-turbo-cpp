#define GPIOC_BRR			(GPPOINTER)(GPIOC_BASE+0x14)
#define GPIOC_LCKR		(GPPOINTER)(GPIOC_BASE+0x18)
#define AHBPERIPH_BASE		(PERIPH_BASE+0x00020000)		//open gpio clock,clock base address
